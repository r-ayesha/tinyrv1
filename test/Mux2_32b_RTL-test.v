//========================================================================
// Mux2_32b_RTL-test
//========================================================================

`include "ece2300/ece2300-test.v"

// ece2300-lint
`include "lab4/Mux2_32b_RTL.v"

module Top();

  //----------------------------------------------------------------------
  // Setup
  //----------------------------------------------------------------------

  CombinationalTestUtils t();

  //----------------------------------------------------------------------
  // Instantiate design under test
  //----------------------------------------------------------------------

  logic [31:0] in0;
  logic [31:0] in1;
  logic        sel;
  logic [31:0] out;

  Mux2_32b_RTL dut
  (
    .in0 (in0),
    .in1 (in1),
    .sel (sel),
    .out (out)
  );

  //----------------------------------------------------------------------
  // check
  //----------------------------------------------------------------------
  // We set the inputs, wait 8 tau, check the outputs, wait 2 tau. Each
  // check will take a total of 10 tau.

  task check
  (
    input logic [31:0] in0_,
    input logic [31:0] in1_,
    input logic        sel_,
    input logic [31:0] out_
  );
    if ( !t.failed ) begin
      t.num_checks += 1;

      in0 = in0_;
      in1 = in1_;
      sel = sel_;

      #8;

      if ( t.n != 0 )
        $display( "%3d: %h %h %b > %h", t.cycles, in0, in1, sel, out );

      `ECE2300_CHECK_EQ( out, out_ );

      #2;

    end
  endtask

  //----------------------------------------------------------------------
  // test_case_1_basic
  //----------------------------------------------------------------------

  task test_case_1_basic();
    t.test_case_begin( "test_case_1_basic" );

    //     in0            in1            sel   out
    check( 32'h0000_0000, 32'h0000_0000, 1'b0, 32'h0000_0000 );
    check( 32'h0000_0000, 32'h0000_0000, 1'b1, 32'h0000_0000 );

    t.test_case_end();
  endtask

  task test_case_1_directed();
    t.test_case_begin( " test_case_1_directed ");

    //     in0            in1            sel   out
    // sel = 0 cases
    check( 32'b00000000000000000000000000000000, 32'b00000000000000000000000000000000, 1'b0,
       32'b00000000000000000000000000000000 );

    check( 32'b11111111111111111111111111111111, 32'b00000000000000000000000000000000, 1'b0,
       32'b11111111111111111111111111111111 );

    check( 32'b10100101101001011010010110100101, 32'b01011010010110100101101001011010, 1'b0,
       32'b10100101101001011010010110100101 );

    check( 32'b00000000000000000000000000000001, 32'b10000000000000000000000000000000, 1'b0,
       32'b00000000000000000000000000000001 );

    check( 32'b00010010001101000101011001111000, 32'b10001001101010111100110111101111, 1'b0,
       32'b00010010001101000101011001111000 );

    check( 32'b00001111000011111111000011110000, 32'b11110000111100001111000011110000, 1'b0,
       32'b00001111000011111111000011110000 );

    // sel = 1 cases
    check( 32'b00000000000000000000000000000000, 32'b00000000000000000000000000000000, 1'b1,
       32'b00000000000000000000000000000000 );

    check( 32'b11111111111111111111111111111111, 32'b00000000000000000000000000000000, 1'b1,
       32'b00000000000000000000000000000000 );

    check( 32'b10100101101001011010010110100101, 32'b01011010010110100101101001011010, 1'b1,
       32'b01011010010110100101101001011010 );

    check( 32'b00000000000000000000000000000001, 32'b10000000000000000000000000000000, 1'b1,
       32'b10000000000000000000000000000000 );

    check( 32'b00010010001101000101011001111000, 32'b10001001101010111100110111101111, 1'b1,
       32'b10001001101010111100110111101111 );

    check( 32'b00001111000011111111000011110000, 32'b11110000111100001111000011110000, 1'b1,
       32'b11110000111100001111000011110000 ); 

    t.test_case_end();
  endtask
  
  //----------------------------------------------------------------------
  // xprop testing
  //----------------------------------------------------------------------

  task test_case_xprop();
    t.test_case_begin( " test_case_xprop ");

    //     in0 in1 sel out
    check( 'x, 'x, 'x, 'x );

    check(  0,  0, 'x, 'x );
    check(  0,  1, 'x, 'x );
    check(  1,  0, 'x, 'x );
    check(  1,  1, 'x, 'x );

    check( 'x,  0,  0, 'x );
    check( 'x,  0,  1,  0 );
    check( 'x,  1,  0, 'x );
    check( 'x,  1,  1,  1 );

    check(  0, 'x,  0,  0 );
    check(  0, 'x,  1, 'x );
    check(  1, 'x,  0,  1 );
    check(  1, 'x,  1, 'x );

    t.test_case_end();
  endtask

  //---------------------------------------------------------------------- 
  // random testing
  //----------------------------------------------------------------------

  logic [31:0] rand_in0;
  logic [31:0] rand_in1;
  logic       rand_sel;
  logic [31:0] rand_out;

  task test_case_random();
    t.test_case_begin( "test_case_random" );

    for ( int i = 0; i < 100; i = i+1 ) begin

      rand_in0 = 32'($urandom(t.seed));
      rand_in1 = 32'($urandom(t.seed));
      rand_sel = 1'($urandom(t.seed));

      if ( rand_sel == 0 )
        rand_out = rand_in0;
      else
        rand_out = rand_in1;

      check( rand_in0, rand_in1, rand_sel, rand_out );

    end

    t.test_case_end();
  endtask

  //----------------------------------------------------------------------
  // main
  //----------------------------------------------------------------------

  initial begin
    t.test_bench_begin();

    if ((t.n <= 0) || (t.n == 1)) test_case_1_basic();
    if ((t.n <= 0) || (t.n == 2)) test_case_1_directed();
    if ((t.n <= 0) || (t.n == 4)) test_case_xprop();
    if ((t.n <= 0) || (t.n == 4)) test_case_random();
    t.test_bench_end();
  end

endmodule

