//========================================================================
// Mux4_32b_RTL-test
//========================================================================

`include "ece2300/ece2300-test.v"

// ece2300-lint
`include "lab4/Mux4_32b_RTL.v"

module Top();

  //----------------------------------------------------------------------
  // Setup
  //----------------------------------------------------------------------

  CombinationalTestUtils t();

  //----------------------------------------------------------------------
  // Instantiate design under test
  //----------------------------------------------------------------------

  logic [31:0] in0;
  logic [31:0] in1;
  logic [31:0] in2;
  logic [31:0] in3;
  logic  [1:0] sel;
  logic [31:0] out;

  Mux4_32b_RTL dut
  (
    .in0 (in0),
    .in1 (in1),
    .in2 (in2),
    .in3 (in3),
    .sel (sel),
    .out (out)
  );

  //----------------------------------------------------------------------
  // check
  //----------------------------------------------------------------------
  // We set the inputs, wait 8 tau, check the outputs, wait 2 tau. Each
  // check will take a total of 10 tau.

  task check
  (
    input logic [31:0] in0_,
    input logic [31:0] in1_,
    input logic [31:0] in2_,
    input logic [31:0] in3_,
    input logic  [1:0] sel_,
    input logic [31:0] out_
  );
    if ( !t.failed ) begin
      t.num_checks += 1;

      in0 = in0_;
      in1 = in1_;
      in2 = in2_;
      in3 = in3_;
      sel = sel_;

      #8;

      if ( t.n != 0 )
        $display( "%3d: %h %h %h %h %b > %h", t.cycles,
                  in0, in1, in2, in3, sel, out );

      `ECE2300_CHECK_EQ( out, out_ );

      #2;

    end
  endtask

  //----------------------------------------------------------------------
  // test_case_1_basic
  //----------------------------------------------------------------------

  task test_case_1_basic();
    t.test_case_begin( "test_case_1_basic" );

    //     in0            in1            in2            in3            sel out
    check( 32'h0000_0000, 32'h0000_0000, 32'h0000_0000, 32'h0000_0000, 0,  32'h0000_0000 );
    check( 32'h0000_0000, 32'h0000_0000, 32'h0000_0000, 32'h0000_0000, 1,  32'h0000_0000 );
    check( 32'h0000_0000, 32'h0000_0000, 32'h0000_0000, 32'h0000_0000, 2,  32'h0000_0000 );
    check( 32'h0000_0000, 32'h0000_0000, 32'h0000_0000, 32'h0000_0000, 3,  32'h0000_0000 );

    t.test_case_end();
  endtask

  //----------------------------------------------------------------------
  // test_case_2_directed
  //----------------------------------------------------------------------

  task test_case_2_directed();
    t.test_case_begin( "test_case_2_directed" );
      check( 32'b1010101010101010_1010101010101010,
            32'b1011101110111011_1011101110111011,
            32'b1100110011001100_1100110011001100,
            32'b1101110111011101_1101110111011101,
            0,
            32'b1010101010101010_1010101010101010 );
      check( 32'b0001000100010001_0001000100010001,
            32'b0010001000100010_0010001000100010,
            32'b0011001100110011_0011001100110011,
            32'b0100010001000100_0100010001000100,
            1,
            32'b0010001000100010_0010001000100010 );
      check( 32'b0000111100001111_0000111100001111,
            32'b1111000011110000_1111000011110000,
            32'b0001001000110100_0101011001111000,
            32'b1000011101100101_0100001100100001,
            2,
            32'b0001001000110100_0101011001111000 );
      check( 32'b1101111010101101_1011101111101111,
            32'b1100101011111110_1111000000001101,
            32'b0001001101010111_1001101111011111,
            32'b0010010001101000_1010110011110000,
            3,
            32'b0010010001101000_1010110011110000 );
    t.test_case_end();
  endtask
  //----------------------------------------------------------------------
  // test_case_3_random
  //----------------------------------------------------------------------

  logic [31:0] rand_in0;
  logic [31:0] rand_in1;
  logic [31:0] rand_in2;
  logic [31:0] rand_in3;
  logic  [1:0] rand_sel;
  logic [31:0] rand_out;

  task test_case_3_random();
    t.test_case_begin( "test_case_3_random" );

    for (int i = 0; i < 50; i = i + 1) begin

      rand_in0 = $urandom(t.seed);
      rand_in1 = $urandom(t.seed);
      rand_in2 = $urandom(t.seed);
      rand_in3 = $urandom(t.seed);
      rand_sel = 2'($urandom(t.seed));

      if ( rand_sel == 2'd0 )
        rand_out = rand_in0;
      else if ( rand_sel == 2'd1 )
        rand_out = rand_in1;
      else if ( rand_sel == 2'd2 )
        rand_out = rand_in2;
      else
        rand_out = rand_in3;

      check( rand_in0, rand_in1, rand_in2, rand_in3, rand_sel, rand_out );

    end

    t.test_case_end();
  endtask

  //----------------------------------------------------------------------
  // test_case_4_xprop
  //----------------------------------------------------------------------

  task test_case_4_xprop();
    t.test_case_begin( "test_case_4_xprop" );
    check( 32'b1010101010101010_1010101010101010,
          32'b1011101110111011_1011101110111011,
          32'b1100110011001100_1100110011001100,
          32'b1101110111011101_1101110111011101,
          2'bxx,
          32'bxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxx );
    check( 32'b0001000100010001_0001000100010001,
          32'b0010001000100010_0010001000100010,
          32'b0011001100110011_0011001100110011,
          32'b0100010001000100_0100010001000100,
          2'b1x,
          32'bxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxx );
    check( 32'b1101111010101101_1011101111101111,
          32'b1100101011111110_1111000000001101,
          32'b0001001101010111_1001101111011111,
          32'b0010010001101000_1010110011110000,
          2'bx1,
          32'bxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxx );
    t.test_case_end();
  endtask

  //----------------------------------------------------------------------
  // main
  //----------------------------------------------------------------------

  initial begin
    t.test_bench_begin();

    if ((t.n <= 0) || (t.n == 1)) test_case_1_basic();
    if ((t.n <= 0) || (t.n == 2)) test_case_2_directed();
    if ((t.n <= 0) || (t.n == 3)) test_case_3_random();
    if ((t.n <= 0) || (t.n == 4)) test_case_4_xprop();

    t.test_bench_end();
  end

endmodule

